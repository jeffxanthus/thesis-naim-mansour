function X = get2dMaximinLHD(n,norm)
% In 2d there exists a method to construct optimal
% LHDs considering the 1-Norm and the Inf-Norm.

if norm == 1
   % 1 norm
   dmin = floor(sqrt(2*n+2));
   t  = zeros(1,dmin);
   X  = zeros(2,n);
   if mod(dmin,2) == 0
      for j = 0:dmin-2
         t(j+2) = t(j+1) + floor((n+0.5*j+0.5*(1-(-1)^j)*0.5*(dmin-1))/(dmin-1));
      end
      tt = diff(t);
      k = 0;
      for j = 0:dmin-2
         for i = 1:tt(j+1)
            k = k+1;
            X(:,k) = [i*(dmin-1)-0.5*j-0.5*(1-(-1)^j)*0.5*(dmin-1) ; t(j+1)+i];
         end
      end
   else
      for j = 0:dmin-1
         t(j+2) = t(j+1) + floor((n+0.5*j+0.5*(1-(-1)^j)*0.5*dmin)/dmin);
      end
      tt = diff(t);
      k = 0;
      for j = 0:dmin-1
         for i = 1:tt(j+1)
            k = k+1;
            X(:,k) = [i*dmin-0.5*j-0.5*(1-(-1)^j)*0.5*dmin ; t(j+1)+i];
         end
      end
   end
   
elseif norm == 3
   % Infinity norm
   dmin = floor(sqrt(n));
   t  = zeros(1,dmin);
   X  = zeros(2,n);

   for j = 0:dmin-1
      t(j+2) = t(j+1) + floor((n+j)/dmin);
   end
   tt = diff(t);
   k = 0;
   for j = 0:dmin-1
      for i = 1:tt(j+1)
         k = k+1;
         X(:,k) = [i*dmin-j ; t(j+1)+i];
      end
   end
   
end


